#pragma once
#include <jmon/export.h>
#include <jmon/extern.h>

/** Jokémon boolean type. */
typedef bool bool_t;

/** Jokémon signed byte type. */
typedef int8_t sbyte_t;

/** Jokémon unsigned byte type. */
typedef uint8_t byte_t;

/** Jokémon signed short type. */
typedef int16_t short_t;

/** Jokémon unsigned short type. */
typedef uint16_t ushort_t;

/** Jokémon fixed-point type. */
typedef fixed fixed_t;

/** Jokémon character type. */
typedef char char_t;

/** Jokémon string type. */
typedef const char_t* string_t;
